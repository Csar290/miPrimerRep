 public class Vectores {
    public static void main(String[] args) {
        
        int arreglo[] = new int[5];
        
        arreglo[0] = 5;
        arreglo[1] = 10;
        arreglo[2] = 15;
        arreglo[3] = 20;
        arreglo[4] = 25;

        for (int i = 0; i < arreglo.length; i++ ) {
            System.out.print(arreglo[i] + " ");
        }
    } 
 }